class Token:
    def __init__(self, type_, value):
        self.type = type_
        self.value = value
    def __repr__(self):
        return f"Token({self.type}, {self.value})"

class Lexer:
    def __init__(self, text):
        self.text = text
        self.pos = 0

    def tokenize(self):
        tokens = []
        while self.pos < len(self.text):
            if self.text[self.pos] in ' \t\r\n':
                self.pos += 1
                continue

            state = 0
            last_accept = None
            last_pos = self.pos
            i = self.pos

            while i < len(self.text) and self.text[i] in TRANS[state]:
                state = TRANS[state][self.text[i]]
                i += 1
                if state in ACCEPT:
                    last_accept = ACCEPT[state]
                    last_pos = i

            if last_accept is None:
                raise SyntaxError(f"Illegal character: {self.text[self.pos]!r}")

            value = self.text[self.pos:last_pos]
            self.pos = last_pos

            if last_accept != 'WS' and last_accept != 'COMMENT':
                tok_type = last_accept
                if tok_type == 'IDENTIFIER':
                    if value == 'if': tok_type = 'IF'
                    elif value == 'else': tok_type = 'ELSE'
                    elif value == 'while': tok_type = 'WHILE'
                    elif value == 'return': tok_type = 'RETURN'
                    elif value == 'int': tok_type = 'INT'
                    elif value == 'void': tok_type = 'VOID'
                    elif value == 'set': tok_type = 'SET'
                tokens.append(Token(tok_type, value))

        return tokens


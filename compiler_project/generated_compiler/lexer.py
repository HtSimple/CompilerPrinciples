class Token:
    def __init__(self, type_, value):
        self.type = type_
        self.value = value
    def __repr__(self):
        return f"Token({self.type}, {repr(self.value)})"

# 关键字映射
KEYWORDS = {
    'begin': 'BEGIN',
    'call': 'CALL',
    'const': 'CONST',
    'do': 'DO',
    'end': 'END',
    'if': 'IF',
    'odd': 'ODD',
    'procedure': 'PROCEDURE',
    'then': 'THEN',
    'var': 'VAR',
    'while': 'WHILE',
}

class Lexer:
    def __init__(self, text):
        self.text = text
        self.pos = 0

    def tokenize(self):
        tokens = []
        while self.pos < len(self.text):
            # 跳过空白
            if self.text[self.pos] in ' \t\r\n':
                self.pos += 1
                continue

            state = 0
            last_accept = None
            last_pos = self.pos
            i = self.pos

            while i < len(self.text):
                ch = self.text[i]
                if state in TRANS and ch in TRANS[state]:
                    state = TRANS[state][ch]
                    i += 1
                    if state in ACCEPT:
                        last_accept = ACCEPT[state]
                        last_pos = i
                else:
                    break

            if last_accept is None:
                raise SyntaxError(f"Illegal character: {self.text[self.pos]!r} at position {self.pos}")

            value = self.text[self.pos:last_pos]
            self.pos = last_pos

            # 跳过空白和注释
            if last_accept in ('WS', 'COMMENT'):
                continue

            # 关键字识别（动态）
            tok_type = last_accept
            if tok_type == 'IDENTIFIER' and value in KEYWORDS:
                tok_type = KEYWORDS[value]

            tokens.append(Token(tok_type, value))

        # 添加结束标记
        tokens.append(Token('$', '$'))
        return tokens

